def solution(num1, num2):
    """
    [문제]
    두 수 num1, num2가 주어질 때
    num1을 num2로 나눈 값에 1000을 곱한 뒤
    소수점 이하를 버린 정수를 반환한다.

    [제한사항]
    - 0 < num1 ≤ 100
    - 0 < num2 ≤ 100

    [풀이]
    - / 연산자는 항상 실수를 반환
    - 계산 결과에 1000을 곱한 뒤
    - // 1 또는 int()를 사용해 소수점을 제거한다
    """

    # 두 수를 나눈 뒤 1000을 곱함
    result = (num1 / num2) * 1000

    # 소수점 이하를 버리고 정수 부분만 반환
    return result // 1

print(solution(2, 4))
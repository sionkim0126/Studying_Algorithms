#include <stdio.h>

// 최대 100개의 정수를 저장할 배열
int d[100];

int main() {
    int i, j, k, n, ans = 0, s = 0;

    // 사용자로부터 배열의 크기 입력받기
    scanf("%d", &n);

    // 배열에 값 입력받기 (배열은 0부터 시작)
    for(i = 0; i < n; i++) {  
        scanf("%d", &d[i]);
    }

    // 최대값을 배열의 첫 번째 값으로 초기화
    ans = d[0];  

    // ⭐ 모든 가능한 부분 배열을 탐색하는 3중 반복문 (O(n³)) ⭐
    for(i = 0; i < n; i++) {  // 부분 배열의 시작점
        for(j = i; j < n; j++) {  // 부분 배열의 끝점
            s = 0;  // 현재 부분 배열의 합을 저장할 변수 초기화
            for(k = i; k <= j; k++) {  // i부터 j까지의 합 계산
                s = s + d[k];
            }
            // 현재 합이 기존 최댓값보다 크다면 갱신
            if(s > ans) {  
                ans = s;
            }
        }
    }

    // 최댓값 출력
    printf("%d\n", ans);
}
